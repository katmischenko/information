# < строго меньше, чем
# <= меньше или равно
# > строго больше, чем
# >= больше или равно
# == равный
# != не равны (! = )
# is идентификация объекта
# is not отрицаемая идентификация объекта
# in членство
# not in аннулированное членство


# Пример
a = 5
b = -10
c = 15

result_1 = a < b  # False
result_2 = a == a  # True
result_3 = a != b  # True
result_4 = b >= c  # False

# Пример
x = -5
y = 10
z = 12

result = x < y and y <= z  # True
